package io.openmarket.order.model;


import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;

import java.util.ArrayList;
import java.util.Date;
import java.util.List;

import static io.openmarket.config.OrderConfig.*;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@DynamoDBTable(tableName = ORDER_DDB_TABLE_NAME)
public class Order {
    @DynamoDBHashKey
    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_ORDER_ID)
    @Builder.Default
    private String orderId = "0";

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_BUYER_ID)
    @Builder.Default
    private String buyerId = "";

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_SELLER_ID)
    @Builder.Default
    private String sellerId = "";

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_CURRENCY)
    @Builder.Default
    private String currency = "";

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_TOTAL_AMOUNT)
    @Builder.Default
    private double total = 0.0;

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_TRANSACTION_ID)
    @Builder.Default
    private String transactionId = "";

    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_STATUS)
    @DynamoDBTypeConvertedEnum
    @Builder.Default
    private OrderStatus status = OrderStatus.PENDING_PAYMENT;

    @DynamoDBTypeConverted(converter = ItemInfoConverter.class)
    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_ITEMS)
    @Builder.Default
    private List<ItemInfo> items = new ArrayList<>();

    @DynamoDBAutoGeneratedTimestamp(strategy = DynamoDBAutoGenerateStrategy.ALWAYS)
    @DynamoDBTypeConvertedTimestamp
    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_UPDATED_AT)
    private Date lastUpdatedAt;

    @DynamoDBAutoGeneratedTimestamp(strategy = DynamoDBAutoGenerateStrategy.CREATE)
    @DynamoDBTypeConvertedTimestamp
    @DynamoDBAttribute(attributeName = ORDER_DDB_ATTRIBUTE_CREATED_AT)
    private Date createdAt;

    @DynamoDBVersionAttribute
    private Integer version;
}